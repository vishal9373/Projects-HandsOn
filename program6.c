#include<stdio.h>

int MultFact(int iNo)
{
	int mult=1;
	for(int olc=1;olc<=iNo/2;olc++)
	{
		if(iNo%olc==0)
		{
			mult=mult*olc;
		}
	}
	return mult;
}

int main()
{
	int iValue=0;
	int iRet=0;

	printf("ENter number: ");
	scanf("%d",&iValue);

	iRet = MultFact(iValue);
	printf("Output: %d\n",iRet);

	return 0;
}

