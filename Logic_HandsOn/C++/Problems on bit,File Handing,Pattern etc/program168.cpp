#include<iostream>
using namespace std;

int Strlen(char *str)
{
	static int iCnt = 0;
	if(*str!='\0')
	{
		iCnt++;
		str++;
		Strlen(str);
	}

	return iCnt;
}

int main()
{
	int iRet = 0;
	char arr[20];

	cout<<"Enter string:";
	scanf("%[^'\n']s",arr);

	iRet = Strlen(arr);

	cout<<"Count is: "<<iRet<<"\n";
}


