#include<iostream>

void Display(int iVal)
{
	static int i=0;
	if(i<iVal)
	{
		cout<<"* ";
		i++;
		Display(iVal);
	}

}

int main()
{
	int iVal = 0;
	cout<<"Enter number: ";
	cin>>iVal;
	
	Display(iVal);
	cout<<"\n";

	return 0;
}


