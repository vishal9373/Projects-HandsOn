//		A recursive program which display below pattern.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

void Display()
{
	static int i=5;
	if(i>0)
	{
		cout<<i<<" ";
		i--;
		Display();
	}

}

int main()
{
	Display();
	cout<<"\n";

	return 0;
}


