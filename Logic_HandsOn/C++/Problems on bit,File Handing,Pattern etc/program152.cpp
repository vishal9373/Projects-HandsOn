//	Program2:
//		A recursive program which accept number from user and return largest digit
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


#include<iostream>
using namespace std;

class Solution
{
	
	public:
		int Max(int iNo)
		{
			static int iMax = 0,iDigit=0;

			if(iNo!=0)
			{
				iDigit = iNo%10;
				if(iMax < iDigit)
				{
					iMax = iDigit;
				}
				iNo = iNo/10;

				Max(iNo);
			}
			return iMax;
		}

};

int main()
{

	int iVal = 0;
	Solution *sObj = new Solution();
	
	cout<<"Enter the number: ";
	cin>>iVal;

	int iRet = sObj->Max(iVal);
	cout<<"Largest Digit is: "<<iRet<<"\n";

	delete sObj;
	return 0;

}


