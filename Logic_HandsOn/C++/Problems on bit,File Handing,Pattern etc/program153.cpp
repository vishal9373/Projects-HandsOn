#include<iostream>
using namespace std;

class Solution
{
	
	public:
		int Small(char *str)
		{
			static int iCount = 0;
			if(*str!='\0')
			{
				if(*str >= 'a' && *str<= 'z')
				{
					iCount++;
				}

				str++;
				Small(str);
			}

			return iCount;
		}

};

int main()
{

	char Arr[20];
	Solution *sObj = new Solution();
	
	cout<<"Enter the string: ";
	scanf("%[^'\n']s",Arr);

	int iRet = sObj->Small(Arr);
	cout<<"Small Characters are: "<<iRet<<"\n";

	delete sObj;
	return 0;

}

