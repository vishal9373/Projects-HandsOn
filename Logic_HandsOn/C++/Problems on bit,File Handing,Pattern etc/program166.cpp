#include<iostream>
using namespace std;

void Display(int iNo)
{
	static int i = 5;
	if(i>0)
	{
		cout<<i<<" * ";
		i--;
		Display(i);
	}
}

int main()
{
	int iVal = 0;
	cout<<"Input: ";
	cin>>iVal;

	Display(iVal);
	cout<<"\n";

	return 0;
}


