#include<iostream>
using namespace std;

class Solution
{
	
	public:
		int WhiteSpace(char *str)
		{
			static int iCount = 0;
			if(*str!='\0')
			{
				if(*str == ' ')
				{
					iCount++;
				}

				str++;
				WhiteSpace(str);
			}

			return iCount;
		}

};

int main()
{

	char Arr[20];
	Solution *sObj = new Solution();
	
	cout<<"Enter the String: ";
	scanf("%[^'\n']s",Arr);

	int iRet = sObj->WhiteSpace(Arr);
	cout<<"WhiteSapces are: "<<iRet<<"\n";

	delete sObj;
	return 0;

}

