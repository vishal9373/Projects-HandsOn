#include<stdio.h>

void Display()
{
	static int i=0;
	static char ch = 'a';
	if(i<6)
	{
		cout<<ch<<" ";
		i++;
		ch++;
		Display();
	}

}

int main()
{
	Display();
	cout<<"\n";

	return 0;
}


