#include<iostream>
using namespace std;

class Solution
{
	public:
		void FactRev(int iNo)
		{
			int arr[5],i=0;
			for(int olc=1;olc<=iNo/2;olc++)
			{
				if(iNo%olc==0)
				{

					arr[i]=olc;
					i++;
				}
			}

			for(int olc=i-1;olc>=0;olc--)
			{
				printf("%d ",arr[olc]);
			}
		}

};	

int main()
{

	int iValue = 0;
	cout<<"Enter number\n";
	cin>>iValue;
	
	Solution *sObj = new Solution();
	sObj->FactRev(iValue);
	
	return 0;
}

