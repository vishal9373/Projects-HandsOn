#include<iostream>
using namespace std;

class Solution
{
	public:
		int MultFact(int iNo)
		{
			int mult=1;
			for(int olc=1;olc<=iNo/2;olc++)
			{
				if(iNo%olc==0)
				{
					mult=mult*olc;
				}
			}
			return mult;
		}
};	

int main()
{

	int iValue=0,iRet=0;
	cout<<"Enter number\n";
	cin>>iValue;
	
	Solution *sObj = new Solution();
	iRet = sObj->MultFact(iValue);

	cout<<"Output: "<<iRet<<"\n";

	return 0;
}

