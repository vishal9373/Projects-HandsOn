#include<iostream>
using namespace std;

class Solution
{
	public:
		void NonFact(int iNo)
		{
			for(int olc=1;olc<=iNo;olc++)
			{		
				if(iNo%olc!=0)
				{
					cout<<olc<<" ";
				}
			}

			cout<<"\n";
		}

};	

int main()
{

	int iValue = 0;
	cout<<"Enter number\n";
	cin>>iValue;
	
	Solution *sObj = new Solution();
	sObj->NonFact(iValue);
	
	return 0;
}

