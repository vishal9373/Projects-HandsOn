//		A program which accept number from user and return summation of all its non factors
//		
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<iostream>
using namespace std;

class Solution
{
	public:
		int SumNonFact(int iNo)
		{
			int sum=0;
			for(int olc=1;olc<=iNo;olc++)
			{				
				if(iNo%olc!=0)
				{
					sum=sum+olc;
				}
			}

			return sum;
		}
};	

int main()
{

	int iValue=0,iRet=0;
	cout<<"Enter number: ";
	cin>>iValue;
	
	Solution *sObj = new Solution();
	iRet = sObj->SumNonFact(iValue);

	cout<<"Output: "<<iRet<<"\n";

	return 0;
}

