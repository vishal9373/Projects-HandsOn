import java.io.*;
import java.util.Scanner;

class Program4
{
	public static void main(String[] args)throws IOException
	{
		System.out.print("Enter the file Name: ");
		Scanner sObj = new Scanner(System.in);
		String str = sObj.nextLine();

		File f = new File(str);

		if(f.exists() != true)
		{
			f.createNewFile();
		}

		System.out.println("Success...");
	
	}
}


