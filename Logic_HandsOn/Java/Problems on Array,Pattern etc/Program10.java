//		a program which accept number from user and return difference between summation of all its factors and non factors.		
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
import java.util.Scanner;

class Solution
{
	int FactDiff(int iNo)
	{
		int iSum1=0,iSum2=0;
		for(int olc=1;olc<=iNo/2;olc++)
		{		
			if(iNo%olc==0)
			{
				iSum1 = iSum1+olc;
			
			}
		}
	
		for(int olc=1;olc<=iNo;olc++)
		{		
			if(iNo%olc!=0)
			{
				iSum2 = iSum2+olc;
			}
		}		
		return iSum1-iSum2;
	}
	
}

class Program10
{
	public  static void main(String[] args)
	{
		System.out.print("Enter Number: ");
		Scanner s = new Scanner(System.in);
		int iNo = s.nextInt();

		Solution sObj = new Solution();
		int iRet = sObj.FactDiff(iNo);
		
		System.out.println("Output: "+iRet);
	}
}

