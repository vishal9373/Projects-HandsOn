#include<stdio.h>

void FactRev(int iNo)
{
	int arr[5],i=0;
	for(int olc=1;olc<=iNo/2;olc++)
	{		
		if(iNo%olc==0){
		
			arr[i]=olc;
			i++;
		}
	}

	for(int olc=i-1;olc>=0;olc--)
	{
		printf("%d ",arr[olc]);
	}
}

int main()
{
	int iValue=0;

	printf("Enter number");
	scanf("%d",&iValue);

	FactRev(iValue);
}

