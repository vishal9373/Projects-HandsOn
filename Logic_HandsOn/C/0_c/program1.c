#include<stdio.h>
void PrintEven(int iNo){

	if(iNo<=0){
	
		return;
	}

	int iCnt=0;
	for(int olc=1;olc<=100;olc++){
	
		if(olc%2==0){
	
			printf("%d ",olc);
			iCnt++;
		}
		if(iCnt==iNo){
		
			return;
		}
	}	
}

int main(){

	int iValue=0;
	printf("Enter number\n");
	scanf("%d",&iValue);
	PrintEven(iValue);

	return 0;
}

