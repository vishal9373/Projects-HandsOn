double KMtoMeter(int iNo)
{
	int iConvert = 0;
	iConvert = iNo*1000;
	return iConvert;
}

int main()
{
	int iValue = 0,iRet = 0;

	printf("Enter distance");
	scanf("%d",&iValue);

	iRet = KMtoMeter(iValue);

	printf("KM to meter conversion is : %d\n",iRet);

	return 0;
}

