#include<stdio.h>

void DisplayASCII()
{

	printf("Decimal\t Hexadecimal \t octal \t Character\n");
	int i=0;
	for(i=0;i<127;i++)
	{
		printf("%d \t\t %x \t %o\t %c\n",i,i,i,i);
	}
}

int main()
{
	DisplayASCII();

	return 0;
}
