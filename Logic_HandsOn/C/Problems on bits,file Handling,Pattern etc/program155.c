#include<stdio.h>

int Reverse(int iNo)
{
	static int iRev=0,iDigit=0;

	if(iNo!=0)
	{
		iDigit = iNo%10;
		iRev = iDigit+iRev*10;
		iNo = iNo/10;

		Reverse(iNo);
	}
	return iRev;
}

int main()
{
	int iVal = 0, iRet = 0;

	printf("Enter the number: ");
	scanf("%d",&iVal);

	iRet = Reverse(iVal);
	printf("Reverse is: %d\n",iRet);

	return 0;
}

