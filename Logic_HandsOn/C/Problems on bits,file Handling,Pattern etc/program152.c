#include<stdio.h>

int Max(int iNo)
{
	static int iMax = 0,iDigit=0;

	if(iNo!=0)
	{
		iDigit = iNo%10;
		if(iMax < iDigit)
		{
			iMax = iDigit;
		}
		iNo = iNo/10;

		Max(iNo);
	}
	return iMax;
}

int main()
{
	int iVal = 0, iRet = 0;

	printf("Enter the number: ");
	scanf("%d",&iVal);

	iRet = Max(iVal);
	printf("Largest Digit is: %d\n",iRet);

	return 0;
}

