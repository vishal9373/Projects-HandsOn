#include<stdio.h>

int Small(int iNo)
{
	static int iMin = 10,iDigit=0;

	if(iNo!=0)
	{
		iDigit = iNo%10;
		if(iMin > iDigit)
		{
			iMin = iDigit;
		}
		iNo = iNo/10;

		Small(iNo);
	}
	return iMin;
}

int main()
{
	int iVal = 0, iRet = 0;

	printf("Enter the number: ");
	scanf("%d",&iVal);

	iRet = Small(iVal);
	printf("Smallest Digit is: %d\n",iRet);

	return 0;
}

