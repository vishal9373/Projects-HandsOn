#include<stdio.h>

void Display(int iNo)
{
	static int i = 5;
	if(i>0)
	{
		printf("%d * ",i);
		i--;
		Display(i);
	}
}

int main()
{
	int iVal = 0;
	printf("Input: ");
	scanf("%d",&iVal);

	Display(iVal);
	printf("\n");

	return 0;
}


