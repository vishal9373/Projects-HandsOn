#include<stdio.h>

void Display(int iVal)
{
	if(iVal>0)
	{
		printf("%d ",iVal);
		iVal--;
		Display(iVal);
	}

}

int main()
{
	int iVal=0;
	printf("Enter number: ");
	scanf("%d",&iVal);
	
	Display(iVal);
	printf("\n");

	return 0;
}


