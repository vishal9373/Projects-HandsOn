#include<stdio.h>

void Display()
{
	static int i=0;
	static char ch = 'A';
	if(i<6)
	{
		printf("%c ",ch);
		i++;
		ch++;
		Display();
	}

}

int main()
{
	Display();
	printf("\n");

	return 0;
}


