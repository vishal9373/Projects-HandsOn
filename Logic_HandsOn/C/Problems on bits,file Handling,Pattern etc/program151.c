#include<stdio.h>

int WhiteSpace(char *str)
{
	static int iCount = 0;
	if(*str!='\0')
	{
		if(*str == ' ')
		{
			iCount++;
		}

		str++;
		WhiteSpace(str);
	}

	return iCount;
}

int main()
{
	char Arr[20];
	int iRet = 0;

	printf("Enter the string: ");
	scanf("%[^'\n']s",Arr);

	iRet = WhiteSpace(Arr);
	printf("WhiteSpaces  are: %d\n",iRet);

	return 0;
}

