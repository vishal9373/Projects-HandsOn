#include<stdio.h>

void Display(int iVal)
{
	static int i=0;
	if(i<iVal)
	{
		printf("%d ",i+1);
		i++;
		Display(iVal);
	}

}

int main()
{
	int iVal=0;
	printf("Enter number: ");
	scanf("%d",&iVal);
	
	Display(iVal);
	printf("\n");

	return 0;
}


