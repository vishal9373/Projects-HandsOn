#include<stdio.h>

void NonFact(int iNo)
{
	for(int olc=1;olc<=iNo;olc++)
	{		
		if(iNo%olc!=0)
		{
			printf("%d ",olc);
		}
	}

	printf("\n");
}

int main(){

	int iValue=0;

	printf("Enter number: ");
	scanf("%d",&iValue);

	NonFact(iValue);

	return 0;
}

