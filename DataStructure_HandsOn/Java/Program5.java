import java.util.*;

class Program5
{
	public static void main(String[] args)
	{
		ArrayList <Integer>lObj = new ArrayList<Integer>();
		int arr[] = new int[]{110,230,320,240};

		for(int i=0;i<4;i++)
		{
			lObj.add(arr[i]);
		}

		Iterator <Integer>iObj = lObj.iterator();
		System.out.print("Input Linked List: [");
		while(iObj.hasNext())
		{
			System.out.print(iObj.next()+", ");
		}

		System.out.println("]");
		System.out.println("Minimum: "+Collections.min(lObj));
		
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Output:
//			Input Linked List: [110, 230, 320, 240, ]
//			Minimum: 110			
