//	Program2:
//		A program which search Last occurrence of particular element from singly linear linked list.
//		Function should return position at which element is found.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>
#include<stdlib.h>

typedef struct node
{
	int data;
	struct node* next;	

}NODE,*PNODE,**PPNODE;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name 	: InsertFirst
// 	Description 	: Used to insert element at first position of Linked List
// 	Parameters 	: Address of First pointer, Integer
// 	return value 	: void
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////	
void InsertFirst(PPNODE Head,int iNo)
{
	PNODE newn = NULL;
	newn = (PNODE)malloc(sizeof(NODE));

	newn->data = iNo;
	newn->next = NULL;

	if(*Head == NULL)
	{
		*Head = newn;
	}
	else
	{
		newn->next = *Head;
		*Head = newn;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Function name   : SearchLastOcc
//      Description     : Used to search the Last Occurance of input element in Given Linked List
//      Parameters      : First Pointer, Integer
//      return value    : Integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

int SearchLastOcc(PNODE Head,int iNo)
{
	int iCount=1,iValue=0;
	while(Head != NULL)
	{
		if(Head->data == iNo)
		{
			iValue = iCount;
		}
		iCount++;	
		Head = Head->next;
	}

	return iValue;

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Function name   : Display
//      Description     : Used to Display elements of Linked List
//      Parameters      : First pointer
//      return value    : void
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

void Display(PNODE Head)
{
	printf("Input Linked List: ");
	while(Head!=NULL)
	{
		printf("|%d|->",Head->data);
		Head = Head->next;
	}

	printf("NULL\n");
}

int main()
{
	PNODE first = NULL;
	int i=0,iValue=0,iRet=0;
	int arr[7]={70,30,50,40,30,20,10};

	for(i=0;i<7;i++)
	{	
		InsertFirst(&first,arr[i]);
	}
	Display(first);

	printf("Input element: ");
	scanf("%d",&iValue);

	iRet = SearchLastOcc(first,iValue);

	printf("Output: %d\n",iRet);
	return 0;
}

