#include<stdio.h>
#include<stdlib.h>

typedef struct node
{
	int data;
	struct node* next;	

}NODE,*PNODE,**PPNODE;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name 	: InsertFirst
// 	Description 	: Used to insert element at first position of Linked List
// 	Parameters 	: Address of First pointer, Integer
// 	return value 	: void
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////	
void InsertFirst(PPNODE Head,int iNo)
{
	PNODE newn = NULL;
	newn = (PNODE)malloc(sizeof(NODE));

	newn->data = iNo;
	newn->next = NULL;

	if(*Head == NULL)
	{
		*Head = newn;
	}
	else
	{
		newn->next = *Head;
		*Head = newn;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Function name   : AdditionEven
//      Description     : Used to Display Prime from Given Linked List
//      Parameters      : First Pointer
//      return value    : Integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

int AdditionEven(PNODE Head)
{
	int iAdd=0;
	while(Head != NULL)
	{
		if((Head->data)%2==0)
		{
			iAdd = iAdd+(Head->data);
		}

		Head=Head->next;
	}
	return iAdd;


}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Function name   : Display
//      Description     : Used to Display elements of Linked List
//      Parameters      : First pointer
//      return value    : void
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

void Display(PNODE Head)
{
	printf("Input Linked List: ");
	while(Head!=NULL)
	{
		printf("|%d|->",Head->data);
		Head = Head->next;
	}

	printf("NULL\n");
}

int main()
{
	PNODE first = NULL;
	int i=0;
	int arr[4]={41,32,20,11};

	for(i=0;i<4;i++)
	{	
		InsertFirst(&first,arr[i]);
	}
	Display(first);
	int iRet = AdditionEven(first);

	printf("Addition of Even is %d\n",iRet);
	return 0;
}

