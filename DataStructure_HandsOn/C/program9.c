#include<stdio.h>
#include<stdlib.h>

typedef struct node
{
	int data;
	struct node* next;	

}NODE,*PNODE,**PPNODE;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name 	: InsertFirst
// 	Description 	: Used to insert element at first position of Linked List
// 	Parameters 	: Address of First pointer, Integer
// 	return value 	: void
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////	
void InsertFirst(PPNODE Head,int iNo)
{
	PNODE newn = NULL;
	newn = (PNODE)malloc(sizeof(NODE));

	newn->data = iNo;
	newn->next = NULL;

	if(*Head == NULL)
	{
		*Head = newn;
	}
	else
	{
		newn->next = *Head;
		*Head = newn;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Function name   : SecMaximum
//      Description     : Used to return second Highest from Given Linked List
//      Parameters      : First Pointer
//      return value    : Integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

int SecMaximum(PNODE Head)
{
	PNODE temp1 = Head;
	PNODE temp2 = Head;
	int iMax1=0,iMax2=0;

	while(temp1 != NULL)
	{
		if(iMax1 < temp1->data)
		{
			iMax1 = temp1->data;
		}

		temp1 = temp1->next;
	}

	while(temp2 != NULL)
	{
		if(iMax1 != temp2->data  && iMax2 < temp2->data)
		{
			iMax2 = temp2->data;
		}

		temp2 = temp2->next;
	}

	return iMax2;
	
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Function name   : Display
//      Description     : Used to Display elements of Linked List
//      Parameters      : First pointer
//      return value    : void
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

void Display(PNODE Head)
{
	printf("Input Linked List: ");
	while(Head!=NULL)
	{
		printf("|%d|->",Head->data);
		Head = Head->next;
	}

	printf("NULL\n");
}

int main()
{
	PNODE first = NULL;
	int i=0;
	int arr[4]={240,320,230,110};

	for(i=0;i<4;i++)
	{	
		InsertFirst(&first,arr[i]);
	}
	Display(first);
	int iRet = SecMaximum(first);

	printf("Second Maximum is %d\n",iRet);
	return 0;
}

