#include<stdio.h>
#include<stdlib.h>

typedef struct node
{
	int data;
	struct node* next;	

}NODE,*PNODE,**PPNODE;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name 	: InsertFirst
// 	Description 	: Used to insert element at first position of Linked List
// 	Parameters 	: Address of First pointer, Integer
// 	return value 	: void
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////	
void InsertFirst(PPNODE Head,int iNo)
{
	PNODE newn = NULL;
	newn = (PNODE)malloc(sizeof(NODE));

	newn->data = iNo;
	newn->next = NULL;

	if(*Head == NULL)
	{
		*Head = newn;
	}
	else
	{
		newn->next = *Head;
		*Head = newn;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Function name   : Maximum
//      Description     : returns the maximum element from Given Linked List
//      Parameters      : First Pointer
//      return value    : Integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

int Maximum(PNODE Head)
{
	int iMax=0;
	while(Head != NULL)
	{
		if(iMax < Head->data)
		{
			iMax = Head->data;
		}
		Head = Head->next;
	}

	return iMax;

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Function name   : Display
//      Description     : Used to Display elements of Linked List
//      Parameters      : First pointer
//      return value    : void
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

void Display(PNODE Head)
{
	printf("Input Linked List: ");
	while(Head!=NULL)
	{
		printf("|%d|->",Head->data);
		Head = Head->next;
	}

	printf("NULL\n");
}

int main()
{
	PNODE first = NULL;
	int i=0,iRet=0;
	int arr[4]={240,320,230,110};

	for(i=0;i<4;i++)
	{	
		InsertFirst(&first,arr[i]);
	}
	Display(first);

	iRet = Maximum(first);

	printf("Maximum: %d\n",iRet);
	return 0;
}

