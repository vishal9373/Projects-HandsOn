#include<stdio.h>
#include<stdlib.h>

typedef struct node
{
	int data;
	struct node* next;	

}NODE,*PNODE,**PPNODE;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name 	: InsertFirst
// 	Description 	: Used to insert element at first position of Linked List
// 	Parameters 	: Address of First pointer, Integer
// 	return value 	: void
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////	
void InsertFirst(PPNODE Head,int iNo)
{
	PNODE newn = NULL;
	newn = (PNODE)malloc(sizeof(NODE));

	newn->data = iNo;
	newn->next = NULL;

	if(*Head == NULL)
	{
		*Head = newn;
	}
	else
	{
		newn->next = *Head;
		*Head = newn;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Function name   : Minimum
//      Description     : returns the minimum element from Given Linked List
//      Parameters      : First Pointer
//      return value    : Integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

int Minimum(PNODE Head)
{
	int iMin = Head->data;
	while(Head != NULL)
	{
		if(iMin > Head->data)
		{
			iMin = Head->data;
		}
		Head = Head->next;
	}

	return iMin;

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//      Function name   : Display
//      Description     : Used to Display elements of Linked List
//      Parameters      : First pointer
//      return value    : void
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 

void Display(PNODE Head)
{
	printf("Input Linked List: ");
	while(Head!=NULL)
	{
		printf("|%d|->",Head->data);
		Head = Head->next;
	}

	printf("NULL\n");
}

int main()
{
	PNODE first = NULL;
	int i=0,iRet=0;
	int arr[5]={640,240,20,230,110};

	for(i=0;i<5;i++)
	{	
		InsertFirst(&first,arr[i]);
	}
	Display(first);

	iRet = Minimum(first);

	printf("Minimum: %d\n",iRet);
	return 0;
}

