/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////#include<stdio.h>

double FhtoCs(float fTemp)
{
	double dConvert=0.0;
	dConvert = (fTemp-32)*5/9;

	return dConvert;
}

int main()
{
	float fValue = 0.0;
	double dRet = 0.0;

	printf("Enter temperature in Fahrenheit: ");
	scanf("%f",&fValue);

	dRet = FhtoCs(fValue);
	printf("%f\n",dRet);

	return 0;
}

